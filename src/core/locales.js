function lookup(source, path) {
  return path.split('.').reduce((value, key) => {
    if (value && typeof value === 'object' && key in value) {
      return value[key];
    }
    return undefined;
  }, source);
}

function format(value, params) {
  if (typeof value !== 'string') return value;
  return value.replace(/\{(\w+)\}/g, (match, key) => {
    const replacement = params?.[key];
    return replacement == null ? match : String(replacement);
  });
}

export function createTranslator(locale = DEFAULT_LOCALE) {
  const activeLocale = AVAILABLE_LOCALES.includes(locale) ? locale : DEFAULT_LOCALE;

  return function t(key, params = {}) {
    const primary = lookup(dictionaries[activeLocale], key);
    const fallback = lookup(dictionaries[DEFAULT_LOCALE], key);
    const value = primary ?? fallback ?? key;
    return format(value, params);
  };
}

export function getLocaleDirection(locale) {
  return AVAILABLE_LOCALES.includes(locale) ? locale : DEFAULT_LOCALE;
}
